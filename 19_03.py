# deve ler o nível do rio (em metros) continuamente.

# * Se o nível for menor que 3m: "Estado Normal".
# * Entre 3m e 5m: "Estado de Alerta".
# * Acima de 5m: "Evacuação Imediata".
# O programa só deve encerrar a leitura se o usuário digitar um valor negativo
# (flag/bandeira de encerramento).

while True:
  nivel_rio = int(input("Nível do rio (em metros): "))
  if nivel_rio < 0:
    print("Encerrando...")
    break
  elif nivel_rio < 3:
    print("Estado Normal")
  elif nivel_rio >= 3 and nivel_rio <= 5:
    print("Estado de Alerta")
  else:
    print("Evacuação Imediata")


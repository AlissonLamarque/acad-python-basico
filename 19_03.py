# 1. O Reflorestador (Loop com Acumulador)
# Em um mini-game de reflorestamento, o jogador deve plantar árvores até atingir
# uma meta de "Biomassa". Crie um programa que peça ao usuário a meta de biomassa
# (em unidades). Depois, use um while para ler sucessivamente o valor de biomassa
# de cada árvore plantada. O programa deve parar assim que a meta for atingida ou
# superada e exibir quantas árvores foram necessárias.

contador = 0
soma_bio = 0

meta_bio = int(input("Meta de biomassa (em unidades): "))

while True:
  contador += 1
  valor_bio = int(input(f"Biomassa da {contador}a árvore: "))
  soma_bio += valor_bio
  if soma_bio >= meta_bio:
    break

print(f"Foram necessárias {contador} árvores para atingir a meta de biomassa.")

# 2. Simulador de Combate: Turnos de RPG
# Em um jogo de turno, um herói tem 100 de HP e enfrenta um monstro com 100 de HP.
# O programa deve pedir ao usuário o dano do ataque do herói e o dano do ataque do
# monstro por rodada. Use um while que continue enquanto ambos estiverem com HP
# acima de zero. Ao final de cada turno, mostre o HP restante de ambos. Se alguém
# chegar a 0, encerre e anuncie o vencedor.

vida_heroi = 100
vida_monstro = 100

while vida_heroi > 0 and vida_monstro > 0:
  print(f"Vida do herói: {vida_heroi}")
  print(f"Vida do monstro: {vida_monstro}")

  dano_heroi = int(input("Dano do ataque do herói: "))
  dano_monstro = int(input("Dano do ataque do monstro: "))

  vida_heroi -= dano_monstro
  vida_monstro -= dano_heroi

if (vida_heroi > 0):
  print("O herói venceu!")
else:
  print("O monstro venceu!")

